void Dev_Gpio_Test(void)
{  
	GPIO_Init(GPIOA, 3, OUTPUT);	
	GPIO_Init(GPIOA, 4, INT_RISE_EDGE);

	GPIO_BitSet(GPIOA, 26);
	GPIO_Init(GPIOA, 26, OUTPUT);

	GPIO_BitClr(GPIOB, 7);
	GPIO_Init(GPIOB, 7, OUTPUT);

	NVIC_EnableIRQ(GPIO_IRQn);
	
	while(1)
	{
		GPIO_BitTog(GPIOA, 3);
		
		_delay_ms(50);
	}
}


void GPIO_Handler(void)
{
   	if(GPIO_PortIntStat(GPIOA) & (1<<4))
	{ 
		DDEBUG("GPIO INTR\n");
		GPIO_BitIntClr(GPIOA, 4);  
	}
}

